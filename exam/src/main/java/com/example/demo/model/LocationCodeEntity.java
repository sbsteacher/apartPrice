package com.example.demo.model;

public class LocationCodeEntity {
	private int inLocationCd;
	private int exLocationCd;
	private String locationNm;
	
	public int getInLocationCd() {
		return inLocationCd;
	}
	public void setInLocationCd(int inLocationCd) {
		this.inLocationCd = inLocationCd;
	}
	public int getExLocationCd() {
		return exLocationCd;
	}
	public void setExLocationCd(int exLocationCd) {
		this.exLocationCd = exLocationCd;
	}
	public String getLocationNm() {
		return locationNm;
	}
	public void setLocationNm(String locationNm) {
		this.locationNm = locationNm;
	}
}
